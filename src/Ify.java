public class Ify {
    public static void main(String[] args) {
        int number1 = 50;
        if(number1>100){
            System.out.println("Liczba 1 większa od 100");
        } else if(number1>0){
            System.out.println("Liczba 1 większa od 0");
        }

        int number2 = 101;
        if(number2>0){
            System.out.println("Liczba 2 większa od 0");
        }
        if(number2>100){
            System.out.println("Liczba 2 większa od 100");
        }
    }
}
